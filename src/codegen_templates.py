def _sid(spec):
    return (spec.get("session_id") or "").strip() or (spec.get("name") or "job")


def _handle_stop(*_a):
    print("\\n[scheduler] stop requested — unwinding running jobs...", flush=True)
    _stop.set()
    for _s in _SCHEDULES:                 # cancel any in-flight run per job/session
        try:
            core.request_cancel(session_id=_sid(_s))
        except Exception:
            pass


def _sleep(seconds):
    """Interruptible sleep in ~1s slices so Ctrl+C / SIGTERM is responsive."""
    slept = 0.0
    while slept < seconds and not _stop.is_set():
        step = min(1.0, seconds - slept)
        time.sleep(step)
        slept += step


def _parse_dt(s):
    """Absolute LOCAL datetime string -> epoch, or None. (Same formats analyze validates.)"""
    for fmt in ("%Y-%m-%d %H:%M:%S", "%Y-%m-%d %H:%M",
                "%Y-%m-%dT%H:%M:%S", "%Y-%m-%dT%H:%M"):
        try:
            return datetime.datetime.strptime(s, fmt).timestamp()
        except ValueError:
            pass
    return None


def _next_daily(hhmm, after):
    """Epoch of the next LOCAL HH:MM[:SS] strictly after `after` (today, else tomorrow)."""
    p = [int(x) for x in hhmm.split(":")]
    h, m = p[0], (p[1] if len(p) > 1 else 0)
    s = p[2] if len(p) > 2 else 0
    cand = datetime.datetime.fromtimestamp(after).replace(
        hour=h, minute=m, second=s, microsecond=0)
    if cand.timestamp() <= after:
        cand += datetime.timedelta(days=1)
    return cand.timestamp()


def _first_due(spec, every, offset, run_at_start):
    """Wall-clock time of the FIRST tick, by the job's strategy (`mode`):
    daily = next `at`; once = the `start_at` instant; interval = offset/run_at_start."""
    now = time.time()
    mode = spec.get("mode") or "interval"
    if mode == "daily":
        return _next_daily((spec.get("at") or "").strip() or "00:00", now)
    if mode == "once":
        t = _parse_dt((spec.get("start_at") or "").strip())
        return t if t is not None else now    # past/unparseable -> fire ASAP
    return now + offset + (0.0 if run_at_start else every)


def _next_due(spec, prev, every):
    """Wall-clock time of the NEXT tick, or None to stop (a 'once' job fires once)."""
    mode = spec.get("mode") or "interval"
    if mode == "once":
        return None                           # single run at start_at
    if mode == "daily":
        return _next_daily((spec.get("at") or "").strip() or "00:00", prev)
    return prev + every                       # drift-free: anchored to the previous DUE time


def _job(spec):
    name = spec.get("name") or "job"
    task = (spec.get("task") or "").strip() or "Run your scheduled task."
    every = max(1.0, float(spec.get("every_seconds", 3600) or 3600))
    offset = max(0.0, float(spec.get("offset_seconds", 0) or 0))
    sid = _sid(spec)
    max_runs = int(spec.get("max_runs", 0) or 0)
    run_at_start = bool(spec.get("run_at_start", True))
    # The agent this job DRIVES. Blank / the graph entry = run the whole agent (B);
    # a different agent name = start the run there (A: separate schedules → separate
    # agents). Passed as core.run(entry=…); None keeps the default entry byte-for-byte.
    _tgt = (spec.get("target") or "").strip()
    entry = _tgt if (_tgt and _tgt != getattr(core, "ENTRY", None)) else None
    due = _first_due(spec, every, offset, run_at_start)   # exact wall-clock timing
    n = 0
    while not _stop.is_set() and (max_runs == 0 or n < max_runs):
        _sleep(max(0.0, due - time.time()))
        if _stop.is_set():
            break
        n += 1
        stamp = time.strftime("%Y-%m-%d %H:%M:%S")
        _via = f" -> {entry}" if entry else ""
        print(f"\\n=== [{name}]{_via} run #{n} @ {stamp} ===", flush=True)
        try:
            out = core.run(task, session_id=sid, entry=entry)   # concurrent-safe (own session)
            print(f"=== [{name}] run #{n} done ===\\n{out}", flush=True)
        except Exception as e:            # one bad tick must NOT kill the job
            print(f"[{name}] run #{n} failed: {e}", flush=True)
        if max_runs and n >= max_runs:
            break
        due = _next_due(spec, due, every)
        if due is None:               # a 'once' job fires exactly once
            break
    print(f"[{name}] stopped after {n} run(s).", flush=True)


def main():
    if not _SCHEDULES:
        print("[scheduler] no schedules configured (config.json 'schedules' is empty).",
              flush=True)
        return
    signal.signal(signal.SIGINT, _handle_stop)
    try:
        signal.signal(signal.SIGTERM, _handle_stop)
    except (ValueError, AttributeError, OSError):
        pass                              # SIGTERM not settable on some platforms/threads
    print(f"[scheduler] @AGENT_NAME@ — {len(_SCHEDULES)} job(s) "
          + "(" + ", ".join(str(s.get("name")) for s in _SCHEDULES) + "). Ctrl+C to stop.",
          flush=True)
    threads = [threading.Thread(target=_job, args=(s,), name=str(s.get("name")),
                                daemon=True) for s in _SCHEDULES]
    for t in threads:
        t.start()
    try:                                  # keep the main thread alive + signal-responsive
        while not _stop.is_set() and any(t.is_alive() for t in threads):
            time.sleep(0.5)
    except KeyboardInterrupt:
        _handle_stop()
    _stop.set()
    for t in threads:
        t.join(timeout=10)
    print("[scheduler] all jobs stopped.", flush=True)


if __name__ == "__main__":
    main()
