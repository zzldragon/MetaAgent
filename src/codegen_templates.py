    # ── settings (edit config.json) / reload / MCP ──────────────────────────
    def on_edit_settings(self):
        dlg = SettingsDialog(self)
        if dlg.exec() != QDialog.Accepted:
            return
        try:
            core.save_config(dlg.collect())
        except Exception as e:
            QMessageBox.warning(self, "Could not save settings", str(e))
            return
        self.append("[settings] saved to config.json and reloaded; new settings "
                    "apply to the next run.")
        self._sig_status.emit("Settings saved.")

    def on_reload_config(self):
        core.reload_config()
        self.append("[reload] config.json reloaded; new settings apply to the "
                    "next run.")
        self._sig_status.emit("Config reloaded.")

    def on_reconnect_mcp(self):
        self.append("[mcp] reconnecting to MCP server(s)...")
        self._sig_status.emit("Reconnecting MCP...")

        def work():
            try:
                if hasattr(core, "reload_config"):
                    core.reload_config()
                core.mcp_reconnect(emit=lambda s: self._sig_append.emit(s))
                self._sig_status.emit("MCP reconnected.")
            except Exception as e:
                self._sig_append.emit(f"[mcp] reconnect failed: {e}")
                self._sig_status.emit("MCP reconnect failed.")

        threading.Thread(target=work, daemon=True).start()

    # ── WebSocket server (runs server.py as a subprocess) ───────────────────
    def on_toggle_server(self, checked):
        if checked:
            self._start_server()
        else:
            self._stop_server()

    def _maybe_autostart_server(self):
        """config.json -> server.autostart: open the WebSocket port on launch (checks
        the Server action -> spawns server.py) instead of waiting for the menu. The
        headless server.py ALWAYS listens on start, so this only affects the desktop
        app. No-op in the frozen build (start server.py separately) or if server.py
        wasn't generated (no WebServer node)."""
        if not core.CONFIG.get("server", {}).get("autostart"):
            return
        if getattr(sys, "frozen", False):
            return
        if not os.path.isfile(os.path.join(_BASE, "server.py")):
            return
        self._srv_action.setChecked(True)     # -> on_toggle_server -> _start_server

    def _start_server(self):
        if self._server_proc and self._server_proc.poll() is None:
            return
        if getattr(sys, "frozen", False):
            QMessageBox.information(
                self, "Not available here",
                "In the packaged app, start the server separately "
                "(python server.py in the agent folder).")
            self._srv_action.setChecked(False)
            return
        self._server_proc = subprocess.Popen(
            [sys.executable, os.path.join(_BASE, "server.py")], cwd=_BASE)
        cfg = core.CONFIG.get("server", {})
        host = cfg.get("host", "127.0.0.1")
        port = cfg.get("port", 8765)
        self.append(f"[server] starting on ws://{host}:{port} — open "
                    f"http://{host}:{port}/ in a browser for the web UI "
                    "(ready in a few seconds)...")
        self._sig_status.emit(f"WebSocket server + web UI: http://{host}:{port}/")

    def _stop_server(self, quiet=False):
        running = bool(self._server_proc and self._server_proc.poll() is None)
        if running:
            self._server_proc.kill()
        self._server_proc = None
        # Only report a stop when one was actually running, so the frozen-app
        # decline path (which reverts the checkable action, re-emitting toggled)
        # doesn't print a spurious "[server] stopped." line.
        if not quiet and running:
            self.append("[server] stopped.")
            self._show_workspace_status()

    def closeEvent(self, event):
        self._stop_server(quiet=True)
        super().closeEvent(event)

