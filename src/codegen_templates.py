# One asyncio.Lock PER SESSION: a user's own turns serialize (no overlapping runs
# in one conversation), but different sessions run CONCURRENTLY. The generated
# runtime forks an isolated per-session state (core.run(session_id=…)), so there's
# no shared run state to protect globally. (The dict grows by one small lock per
# distinct session for the process lifetime — negligible.)
_SESSION_LOCKS: dict = {}
_CONNS = {"n": 0}           # live connection count (for MAX_CONNS)


def _session_lock(sid: str):
    lk = _SESSION_LOCKS.get(sid)
    if lk is None:
        lk = asyncio.Lock()
        _SESSION_LOCKS[sid] = lk
    return lk


def _msg(**fields) -> str:
    return json.dumps(fields, ensure_ascii=False)


# ── HITL bridge: prompt the browser and block the agent's worker thread ─────
# The agent runs on a background thread; when it hits a tool-confirm or review
# checkpoint we push a frame to the client and block that thread on an Event
# until the async handle() loop delivers the client's hitl_response (or a
# cancel/disconnect resolves it with a safe default).
_HITL_PENDING: dict = {}             # req_id -> {"event","result","default","owner"}
_HITL_BLOCK = threading.Lock()
_HITL_SEQ = {"n": 0}


def _next_hitl_id() -> int:
    with _HITL_BLOCK:
        _HITL_SEQ["n"] += 1
        return _HITL_SEQ["n"]


def _resolve_pending(owner=None) -> None:
    """Unblock worker threads waiting on a HITL answer, applying each request's
    safe default. `owner` (a session id) scopes it to ONE session's prompts — so a
    cancel / disconnect / run-end on one connection never force-resolves another
    concurrent session's live prompt. owner=None resolves all (process shutdown)."""
    with _HITL_BLOCK:
        ids = [i for i, s in _HITL_PENDING.items()
               if owner is None or s.get("owner") == owner]
        slots = [_HITL_PENDING.pop(i) for i in ids]
    for slot in slots:
        slot["result"] = slot["default"]
        slot["event"].set()


def _hitl_request(loop, queue, kind: str, payload: dict, default, owner=None):
    """(worker thread) send a HITL prompt to the browser, then block until the
    client answers — or a cancel/disconnect resolves it with `default`. `owner` is
    the session id, so only this session's answers / resolutions apply to it."""
    req_id = _next_hitl_id()
    slot = {"event": threading.Event(), "result": default, "default": default,
            "owner": owner}
    with _HITL_BLOCK:
        _HITL_PENDING[req_id] = slot
    loop.call_soon_threadsafe(queue.put_nowait, (kind, dict(payload, id=req_id)))
    slot["event"].wait()
    with _HITL_BLOCK:
        _HITL_PENDING.pop(req_id, None)
    return slot["result"]


async def _run_task(ws, task: str, images=None, session_id=None) -> None:
    loop = asyncio.get_running_loop()
    queue: asyncio.Queue = asyncio.Queue()

    def emit(text: str) -> None:
        loop.call_soon_threadsafe(queue.put_nowait, ("trace", text))

    def on_token(delta: str) -> None:
        loop.call_soon_threadsafe(queue.put_nowait, ("token", delta))

    # Per-CONNECTION HITL handlers, passed INTO core.run (which installs them on
    # this run's own state) — a global set_confirm_handler would race across
    # concurrent connections. When auto-allowing, approve without a round-trip.
    if AUTO_ALLOW:
        def _confirm(tool_name, args):
            return {"decision": "allow"}

        def _review(prompt, content, choices=None):
            # route mode: auto-pick the first branch; gate mode: auto-approve
            return {"decision": (choices[0] if choices else "approve"),
                    "content": content, "feedback": ""}
    else:
        def _confirm(tool_name, args):
            prompt = ("Allow high-risk tool '" + str(tool_name)
                      + "' with arguments:" + chr(10)
                      + json.dumps(args, ensure_ascii=False, indent=2))
            return bool(_hitl_request(
                loop, queue, "hitl_confirm", {"prompt": prompt}, default=False,
                owner=session_id))

        def _review(prompt, content, choices=None):
            # choices set => route mode: the browser shows a branch picker and
            # returns the chosen successor name in `decision`. Disconnect defaults
            # to the first branch (route) / reject (gate) so the run still finishes.
            _pay = {"prompt": prompt, "content": content or ""}
            if choices:
                _pay["choices"] = list(choices)
                _dflt = {"decision": choices[0], "content": content or "",
                         "feedback": "client disconnected"}
            else:
                _dflt = {"decision": "reject", "content": content or "",
                         "feedback": "client disconnected"}
            res = _hitl_request(loop, queue, "hitl_review", _pay,
                                default=_dflt, owner=session_id)
            return res if isinstance(res, dict) else {
                "decision": (choices[0] if choices else "approve"),
                "content": content or "", "feedback": ""}

    def work() -> None:
        try:
            # browser images arrive as base64 data URLs → neutral parts, passed
            # into run() so they land on THIS session's (possibly forked) state
            parts = []
            if images and hasattr(core, "decode_data_url"):
                for url in images:
                    try:
                        parts.append(core.decode_data_url(url))
                    except Exception:
                        pass
            before = _workspace_snapshot(session_id)
            result = core.run(task, emit=emit, on_token=on_token,
                              session_id=session_id, on_confirm=_confirm,
                              on_review=_review, image_parts=parts)
            produced = []
            for p, mt in _workspace_snapshot(session_id).items():
                if before.get(p) != mt:          # new or modified this run
                    try:
                        produced.append({"path": p, "name": os.path.basename(p),
                                         "size": os.path.getsize(p)})
                    except OSError:
                        pass
            loop.call_soon_threadsafe(queue.put_nowait, ("result", (result, produced)))
        except Exception as e:
            loop.call_soon_threadsafe(
                queue.put_nowait, ("error", f"{type(e).__name__}: {e}"))

    threading.Thread(target=work, daemon=True).start()
    while True:
        kind, payload = await queue.get()
        if kind == "token":
            await ws.send(_msg(type="token", text=payload))
        elif kind == "trace":
            await ws.send(_msg(type="trace", text=payload))
        elif kind in ("hitl_confirm", "hitl_review"):
            # forward the prompt; the worker thread is blocked awaiting the
            # client's hitl_response (handled live by handle()). Keep looping.
            await ws.send(_msg(type=kind, **payload))
        elif kind == "result":
            text, produced = payload
            await ws.send(_msg(type="result", result=text, files=produced))
            return
        elif kind == "error":
            await ws.send(_msg(type="error", error=payload))
            return


async def _run_with_lock(ws, task: str, images=None, session_id=None) -> None:
    async with _session_lock(session_id):      # serialize a session's OWN turns;
        try:                                    # different sessions run concurrently
            await _run_task(ws, task, images, session_id)
        except Exception:
            pass                               # e.g. the socket closed mid-stream
        finally:
            _resolve_pending(session_id)        # unblock only THIS session's prompt


async def handle(ws):
    if MAX_CONNS and _CONNS["n"] >= MAX_CONNS:
        await ws.send(_msg(type="error", error="server busy — too many connections"))
        await ws.close()
        return
    _CONNS["n"] += 1
    # Per-connection conversation id → each client gets its OWN history/memory
    # (no cross-talk between simultaneous browser tabs / gateway users). A client
    # may pin a stable id via the task frame's "session_id" (e.g. a gateway keying
    # by end-user) so a conversation survives reconnects; otherwise this ephemeral
    # per-connection id is used.
    conn_sid = "ws-" + os.urandom(8).hex()
    authed = not TOKEN
    _vision = bool(getattr(core, "entry_vision", lambda: False)())
    _ws = (core.get_workspace(session_id=conn_sid)
           if hasattr(core, "get_workspace") else [])
    run_task = None
    run_sid = conn_sid          # the session id of this connection's in-flight run
    try:
        await ws.send(_msg(type="hello", agent="@AGENT_NAME@",
                           auth_required=not authed, vision=_vision, workspace=_ws))
        async for raw in ws:
            try:
                msg = json.loads(raw)
            except json.JSONDecodeError:
                await ws.send(_msg(type="error", error="invalid JSON"))
                continue
            mtype = msg.get("type")
            if not authed:
                if mtype == "auth" and msg.get("token") == TOKEN:
                    authed = True
                    await ws.send(_msg(type="auth_ok"))
                else:
                    await ws.send(_msg(type="error", error="auth required"))
                    await ws.close()
                    return
                continue
            if mtype == "ping":
                await ws.send(_msg(type="pong"))
            elif mtype == "get_workspace":
                folders = (core.get_workspace(session_id=run_sid)
                           if hasattr(core, "get_workspace") else [])
                await ws.send(_msg(type="workspace", folders=folders))
            elif mtype == "set_workspace":     # set the folder agent files go to
                provided = [p for p in (msg.get("folders") or [])
                            if isinstance(p, str) and p.strip()]
                if hasattr(core, "set_workspace"):
                    core.set_workspace(provided, session_id=run_sid)   # per-session
                folders = (core.get_workspace(session_id=run_sid)
                           if hasattr(core, "get_workspace") else [])
                if provided and not folders:
                    await ws.send(_msg(type="trace",
                                       text="[workspace] path not found on the server: "
                                            + "; ".join(provided)))
                else:
                    await ws.send(_msg(type="trace", text="[workspace] "
                                       + ("set to " + "; ".join(folders) if folders else "cleared")))
                await ws.send(_msg(type="workspace", folders=folders))
            elif mtype == "hitl_response":     # answer to a confirm/review prompt
                slot = None
                with _HITL_BLOCK:
                    slot = _HITL_PENDING.get(msg.get("id"))
                # only answer prompts THIS connection owns — a connection must not
                # resolve another session's HITL slot (confused-deputy)
                if slot is not None and slot.get("owner") == run_sid:
                    slot["result"] = msg.get("result")
                    slot["event"].set()
            elif mtype == "cancel":            # Stop button in the web UI
                if hasattr(core, "request_cancel"):
                    core.request_cancel(session_id=run_sid)   # only THIS user's run
                    await ws.send(_msg(type="trace",
                                       text="[cancelling] stopping soon…"))
                _resolve_pending(run_sid)      # unblock THIS session's HITL prompt
            elif mtype == "task":
                # run in the background so this loop keeps reading (cancel/ping)
                if run_task and not run_task.done():
                    await ws.send(_msg(type="error",
                                       error="a run is already in progress"))
                    continue
                task = (msg.get("task") or "").strip()
                if not task:
                    await ws.send(_msg(type="error", error="empty task"))
                    continue
                images = msg.get("images") or []
                run_sid = (str(msg.get("session_id") or "").strip()) or conn_sid
                run_task = asyncio.create_task(
                    _run_with_lock(ws, task, images, run_sid))
            else:
                await ws.send(_msg(type="error", error=f"unknown type: {mtype}"))
    except websockets.exceptions.ConnectionClosed:
        pass                    # client vanished (tab closed / reset / proxy) — normal, not an error
    finally:
        _CONNS["n"] -= 1        # free the connection slot (MAX_CONNS)
        # stop any in-flight run if the client disconnects (only this session's)
        if (run_task and not run_task.done()
                and hasattr(core, "request_cancel")):
            core.request_cancel(session_id=run_sid)
        _resolve_pending(run_sid)   # unblock only THIS session's HITL waiter
        # drop this connection's per-session lock if idle, so a long-lived server
        # serving many short connections doesn't accumulate locks forever
        _lk = _SESSION_LOCKS.get(run_sid)
        if _lk is not None and not _lk.locked():
            _SESSION_LOCKS.pop(run_sid, None)


DOWNLOAD_MAX = 50 * 1024 * 1024     # cap a single web download at 50 MB


def _ws_realdirs(session_id=None) -> list:
    out = []
    for f in (core.get_workspace(session_id=session_id)
              if hasattr(core, "get_workspace") else []):
        try:
            out.append(os.path.realpath(f))
        except OSError:
            pass
    return out


def _safe_under_workspace(path: str):
    """realpath of `path` if it is a regular file inside a workspace folder,
    else None. realpath + commonpath blocks '..' traversal — only files under a
    configured workspace folder are ever served."""
    try:
        rp = os.path.realpath(path)
    except OSError:
        return None
    if not os.path.isfile(rp):
        return None
    for fr in _ws_realdirs():
        try:
            if os.path.commonpath([rp, fr]) == fr:
                return rp
        except ValueError:              # different drives (Windows)
            continue
    return None


def _list_workspace_files(limit: int = 500, session_id=None) -> list:
    items = []
    for fr in _ws_realdirs(session_id):
        for root, dirs, files in os.walk(fr):
            dirs[:] = [d for d in dirs if not d.startswith(".")]
            for name in sorted(files):
                if name.startswith("."):
                    continue
                full = os.path.join(root, name)
                try:
                    size = os.path.getsize(full)
                except OSError:
                    continue
                items.append({"path": full, "size": size,
                              "name": os.path.relpath(full, fr).replace(os.sep, "/")})
                if len(items) >= limit:
                    return items
    return items


def _workspace_snapshot(session_id=None) -> dict:
    """{abspath: mtime} of all workspace files — diffed before/after a run to
    surface the files that run produced."""
    snap = {}
    for it in _list_workspace_files(limit=10000, session_id=session_id):
        try:
            snap[it["path"]] = os.path.getmtime(it["path"])
        except OSError:
            pass
    return snap


def _authed_http(qs) -> bool:
    return (not TOKEN) or (qs.get("token", [""])[0] == TOKEN)


def process_request(connection, request):
    """Serve the chat web UI plus workspace file listing/downloads on plain HTTP
    GETs; let WebSocket upgrades continue to the handler."""
    if "Upgrade" in request.headers:
        return None
    parts = urlsplit(request.path)
    path, qs = parts.path, parse_qs(parts.query)
    if path in ("/", "/index.html"):
        response = connection.respond(http.HTTPStatus.OK, INDEX_HTML)
        try:
            del response.headers["Content-Type"]
        except KeyError:
            pass
        response.headers["Content-Type"] = "text/html; charset=utf-8"
        return response
    if path == "/files":
        if not _authed_http(qs):
            return connection.respond(http.HTTPStatus.UNAUTHORIZED, "auth required")
        body = json.dumps({"files": _list_workspace_files()}, ensure_ascii=False)
        resp = connection.respond(http.HTTPStatus.OK, body)
        try:
            del resp.headers["Content-Type"]
        except KeyError:
            pass
        resp.headers["Content-Type"] = "application/json; charset=utf-8"
        return resp
    if path == "/download":
        if not _authed_http(qs):
            return connection.respond(http.HTTPStatus.UNAUTHORIZED, "auth required")
        rp = _safe_under_workspace(unquote((qs.get("path") or [""])[0]))
        if not rp:
            return connection.respond(http.HTTPStatus.NOT_FOUND, "not in workspace")
        try:
            if os.path.getsize(rp) > DOWNLOAD_MAX:
                return connection.respond(http.HTTPStatus.FORBIDDEN,
                                          "file too large for web download")
            with open(rp, "rb") as fh:
                data = fh.read()
        except OSError:
            return connection.respond(http.HTTPStatus.NOT_FOUND, "not found")
        headers = Headers()
        headers["Content-Type"] = mimetypes.guess_type(rp)[0] or "application/octet-stream"
        headers["Content-Length"] = str(len(data))
        headers["Content-Disposition"] = (
            'attachment; filename="%s"' % os.path.basename(rp).replace('"', "_"))
        return Response(int(http.HTTPStatus.OK), "OK", headers, data)
    if path == "/zip":
        if not _authed_http(qs):
            return connection.respond(http.HTTPStatus.UNAUTHORIZED, "auth required")
        files = _list_workspace_files(limit=2000)
        if not files:
            return connection.respond(http.HTTPStatus.NOT_FOUND, "no files in workspace")
        if sum(f["size"] for f in files) > DOWNLOAD_MAX:
            return connection.respond(http.HTTPStatus.FORBIDDEN,
                                      "workspace too large to zip for web download")
        buf = io.BytesIO()
        seen = {}
        with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as z:
            for f in files:
                rp = _safe_under_workspace(f["path"])
                if not rp:
                    continue
                arc = f["name"]
                if arc in seen:                 # de-collide names across folders
                    seen[arc] += 1
                    base, ext = os.path.splitext(arc)
                    arc = "%s_%d%s" % (base, seen[arc], ext)
                else:
                    seen[arc] = 0
                try:
                    z.write(rp, arc)
                except OSError:
                    pass
        data = buf.getvalue()
        headers = Headers()
        headers["Content-Type"] = "application/zip"
        headers["Content-Length"] = str(len(data))
        headers["Content-Disposition"] = 'attachment; filename="workspace.zip"'
        return Response(int(http.HTTPStatus.OK), "OK", headers, data)
    return connection.respond(http.HTTPStatus.NOT_FOUND, "not found")


